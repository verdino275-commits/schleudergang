function applyLang(){
  // Landing screen
  const logoEl=document.querySelector('.logo-sub');
  if(logoEl)logoEl.textContent=t('subtitle');
  const flabelEl=document.querySelector('.flabel');
  if(flabelEl)flabelEl.textContent=t('players_label');
  const addBtn=document.getElementById('btn-add-p');
  if(addBtn)addBtn.textContent=t('add_player');
  // Game screen tabs
  const tlog=document.getElementById('tab-log');
  if(tlog)tlog.innerHTML=t('log_tab');
  const tchat=document.getElementById('tab-chat');
  if(tchat)tchat.innerHTML=t('chat_tab');
  const ci=document.getElementById('chat-input');
  if(ci)ci.placeholder=t('chat_placeholder');
  // Rules modal
  const rm=document.querySelector('.modal-card h3');
  if(rm)rm.textContent=t('rules_title');
  updateRules();
  // Update lang buttons
  document.querySelectorAll('.lang-btn').forEach(b=>{
    b.classList.toggle('active', b.dataset.lang===LANG);
  });
}

function updateRules(){
  const modal=document.querySelector('.modal-card');
  if(!modal)return;
  const ruleKeys=['r_players','r_enter','r_schranke','r_six','r_arrow','r_takt','r_heraus','r_tausch','r_schlA','r_schlB','r_kick','r_becher_enter','r_becher','r_b5','r_ziel','r_ssp','r_bot','r_quit'];
  const rules=modal.querySelectorAll('.rule');
  rules.forEach((el,i)=>{ if(ruleKeys[i]) el.textContent=t(ruleKeys[i]); });
  const okBtn=modal.querySelector('.bbtn.purple');
  if(okBtn)okBtn.textContent=t('rules_ok');
}


function showLangMenu(){
  const m=document.getElementById('lang-menu');
  if(m)m.style.display=m.style.display==='none'?'block':'none';
}
function hideLangMenu(){
  const m=document.getElementById('lang-menu');
  if(m)m.style.display='none';
}
document.addEventListener('click',e=>{
  const m=document.getElementById('lang-menu');
  if(m&&m.style.display!=='none'&&!m.contains(e.target)&&!e.target.closest('.icon-btn[onclick*=LangMenu]'))
    hideLangMenu();
});


